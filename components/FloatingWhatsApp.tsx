export function FloatingWhatsApp() {
  const { theme, resolvedTheme } = useTheme();
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
  }, []);

  const isDark = mounted ? (resolvedTheme ?? theme) === "dark" : true;
  const waUrl = `https://wa.me/${WHATSAPP_NUMBER}?text=${encodeURIComponent(DEFAULT_MESSAGE)}`;

  return (
    <motion.div
      initial={{ opacity: 0, scale: 0.8 }}
      animate={{ opacity: 1, scale: 1 }}
      transition={{ duration: 0.3, delay: 0.5 }}
      className="fixed bottom-6 right-6 z-30 sm:bottom-8 sm:right-8"
    >
      <Link
        href={waUrl}
        target="_blank"
        rel="noopener noreferrer"
        aria-label="Chat on WhatsApp"
        className="group relative flex items-center justify-center"
      >
        <motion.span
          whileHover={{ scale: 1.05 }}
          whileTap={{ scale: 0.95 }}
          className="relative flex h-14 w-14 items-center justify-center rounded-2xl shadow-lg sm:h-16 sm:w-16"
          style={{
            background: isDark
              ? "linear-gradient(135deg, rgba(79, 125, 243, 0.9) 0%, rgba(34, 211, 238, 0.85) 100%)"
              : "linear-gradient(135deg, #4F7DF3 0%, #22D3EE 100%)",
            boxShadow: isDark
              ? "0 0 24px rgba(79, 125, 243, 0.4), 0 0 12px rgba(34, 211, 238, 0.2)"
              : "0 4px 24px rgba(79, 125, 243, 0.35), 0 0 16px rgba(34, 211, 238, 0.15)",
          }}
        >
          <span
            className="absolute inset-0 rounded-2xl opacity-0 transition-opacity duration-200 group-hover:opacity-100"
            style={{
              background: isDark
                ? "linear-gradient(135deg, rgba(79, 125, 243, 0.3) 0%, rgba(34, 211, 238, 0.2) 100%)"
                : "linear-gradient(135deg, rgba(79, 125, 243, 0.2) 0%, rgba(34, 211, 238, 0.15) 100%)",
            }}
          />
          <WhatsAppIcon className="relative h-7 w-7 text-white sm:h-8 sm:w-8" />
        </motion.span>
        {/* Tooltip */}
        <span
          className="pointer-events-none absolute right-full mr-3 hidden whitespace-nowrap rounded-lg px-3 py-2 text-sm font-medium opacity-0 shadow-lg transition-all duration-200 group-hover:opacity-100 sm:block"
          style={{
            background: isDark ? "rgba(11, 15, 25, 0.95)" : "rgba(255, 255, 255, 0.95)",
            color: isDark ? "#fff" : "#0b0f19",
            border: isDark ? "1px solid rgba(255,255,255,0.08)" : "1px solid rgba(0,0,0,0.06)",
            boxShadow: isDark ? "0 0 20px rgba(79, 125, 243, 0.15)" : "0 4px 20px rgba(0,0,0,0.1)",
          }}
        >
          Chat on WhatsApp
        </span>
      </Link>
    </motion.div>
  );
}
